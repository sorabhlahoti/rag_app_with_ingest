@inngest_client.create_function(
    fn_id="RAG: Ingest PDF",
    # Event that triggers this function
    trigger=inngest.TriggerEvent(event="rag/ingest_pdf"),
)
async def rag_ingest_pdf(ctx: inngest.Context):
    
    def _load(ctx:inngest.Context)-> RAGChunkAndSrc:
        pdf_path=ctx.event.data["pdf_path"]
        source_id=ctx.event.data.get("source_id",pdf_path)
        chunks=load_and_chunk_pdf(pdf_path)
        print(chunks)
        return RAGChunkAndSrc(chunk=chunks,source_id=source_id)

    def _upsert(chunks_and_src:RAGChunkAndSrc)->RAGUpsertResult:
        
        chunks=chunks_and_src.chunk
        source_id=chunks_and_src.source_id
        vecs=embed_texts(chunks)
        ids=[str(uuid.uuid5(uuid.NAMESPACE_URL,f"{source_id}:{i}")) for i in range(len(chunks))]
        payload=[{"source":source_id,"text":chunks[i]} for i in range(len(chunks))]
        
        QdrantStorage().upsert(ids,vecs,payload)
        return RAGUpsertResult(ingested=len(chunks))
    
    chunks_and_src=await ctx.step.run("load-and-chunk",lambda: _load(ctx),output_type=RAGChunkAndSrc)
    
    ingested=await ctx.step.run("embed-and-upsert",lambda:_upsert(chunks_and_src),output_type=RAGUpsertResult)
    return ingested.model_dump()


@inngest_client.create_function(
    fn_id="RAG: Query PDF",
    trigger=inngest.TriggerEvent(event="rag/query_pdf_ai")
)
async def rag_query_pdf_ai(ctx:inngest.Context):
    
    def _search(question:str,top_k:int=5)->RAGSearchResult:
        query_vec=embed_texts([question])[0]
        store=QdrantStorage()
        found=store.search(query_vec,top_k)
        return RAGSearchResult(contexts=found["contexts"],sources=found["sources"])
        
    
    question=ctx.event.data["question"]
    top_k=int(ctx.event.data.get("top_k",5))
    
    found=await ctx.step.run("embed-and-search",lambda: _search(question,top_k),output_type=RAGSearchResult)
    
    context_block="\n\n".join(f"- {c}" for c in found.contexts)
    user_content=(
        "Use the following context to answer the question. \n\n"
        f"Context:\n{context_block}\n\n"
        f"Question:{question} \n"
        "Answer concisely using the context above."
    )

    def _ollama_chat_sync():
        resp = ollama.chat(
            model="gemma3:1b",
            messages=[
                {"role": "system", "content": "You answer questions using only the provided context."},
                {"role": "user", "content": user_content},
            ],
            options={"temperature": 0.2},
        )
    
        # For ollama.chat, resp["message"]["content"] contains text
        return resp["message"]["content"].strip()


    answer = await ctx.step.run("llm-answer", _ollama_chat_sync)
    
    return {"answer": answer, "sources": found.sources, "num_contexts": len(found.contexts)}


    
    
    

